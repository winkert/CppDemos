        public static (double[][] X, double[][] Y) MakeMulDataset(int n, double maxAbsInput, bool includeNegatives, Random rng)
        {
            // Scale to [-1,1] for inputs; target uses consistent scaling
            double scale = maxAbsInput; // original inputs ∈ [-maxAbsInput, maxAbsInput]
            var X = new double[n][];
            var Y = new double[n][];

            for (int i = 0; i < n; i++)
            {
                double a = rng.NextDouble() * scale;
                double b = rng.NextDouble() * scale;

                if (includeNegatives)
                {
                    a *= rng.Next(2) == 0 ? 1 : -1;
                    b *= rng.Next(2) == 0 ? 1 : -1;
                }

                // -Normalize inputs to [-1,1]- Trainer does this part
                double aN = a;
                double bN = b;

                // Target scaled consistently: (a*b)/scale^2
                double tN = (a * b);

                X[i] = [aN, bN];
                Y[i] = [tN];
            }

            return (X, Y);
        }
        public static (double[][] X, double[][] Y) MakeAddDataset(int n, double maxAbsInput, bool includeNegatives, Random rng)
        {
            // Scale to [-1,1] for inputs; target uses consistent scaling
            double scale = maxAbsInput; // original inputs ∈ [-maxAbsInput, maxAbsInput]
            var X = new double[n][];
            var Y = new double[n][];

            for (int i = 0; i < n; i++)
            {
                double a = rng.NextDouble() * scale;
                double b = rng.NextDouble() * scale;

                if (includeNegatives)
                {
                    a *= rng.Next(2) == 0 ? 1 : -1;
                    b *= rng.Next(2) == 0 ? 1 : -1;
                }

                // Normalize inputs to [-1,1]
                double aN = a / scale;
                double bN = b / scale;

                // Target scaled consistently: (a*b)/scale^2
                double tN = (a + b) / (scale);

                X[i] = [aN, bN];
                Y[i] = [tN];
            }

            return (X, Y);
        }


